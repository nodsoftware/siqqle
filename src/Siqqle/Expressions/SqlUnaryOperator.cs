namespace Siqqle.Expressions;

/// <summary>
/// Represents all available unary operators in SQL.
/// </summary>
public enum SqlUnaryOperator
{
    /// <summary>
    /// Represents the logical <b>IS NULL</b> operator.
    /// </summary>
    IsNull,

    /// <summary>
    /// Represents the logical <b>IS NOT NULL</b> operator.
    /// </summary>
    IsNotNull,
}
